<template>
    <div class="regisztracio">
        <div class="container">

            <div class="row margin">
                <div class="col-6 mx-auto">

                    <div class="card login-card mb-3">
                        <div class="card-header h3">Regisztrácó</div>
                        <div class="card-body">

                            <div class="alert alert-danger" role="alert" v-if="alerts.length" >
                                <ul>
                                    <li v-for="a in alerts" :key="a">
                                        {{ a }}
                                    </li>
                                </ul>
                            </div>

                            <form @submit.prevent="registration()">
                                <!--<div class="form-group">-->
                                    <!--<label for="exampleInputEmail1">Felhasználónév</label>-->
                                    <!--<input type="text" class="form-control" placeholder="Felhasználónév">-->
                                <!--</div>-->
                                <div class="form-group">
                                    <label for="exampleInputEmail1">Email address</label>
                                    <input v-model="email" type="email" class="form-control" id="exampleInputEmail1" aria-describedby="emailHelp" placeholder="Enter email">
                                    <small id="emailHelp" class="form-text text-muted">We'll never share your email with anyone else.</small>
                                </div>
                                <div class="form-group">
                                    <label for="exampleInputPassword1">Jelszó</label>
                                    <input v-model="password" type="password" class="form-control" id="exampleInputPassword1" placeholder="Password">
                                </div>
                                <div class="form-group">
                                    <label for="exampleInputPassword2">Jelszó még egyszer</label>
                                    <input v-model="password2" type="password" class="form-control" id="exampleInputPassword2" placeholder="Password">
                                </div>



                                <!--<div class="my-3 text-center">-->
                                    <!--<button type="submit" class="btn loginBtn loginBtn&#45;&#45;facebook py-2"><i class="fa fa-facebook pr-2"></i>Facebook bejelentkezés</button>-->
                                    <!--<button type="submit" class="btn loginBtn loginBtn&#45;&#45;google py-2"><i class="fa fa-google pr-2"></i>Google bejelentkezés</button>-->
                                <!--</div>-->

                                <div class="card-footer bg-transparent"></div>
                                <button type="submit"
                                        class="btn btn-primary btn-lg btn-block"
                                        role="button">
                                    Regisztráció
                                </button>
                            </form>

                        </div>
                    </div>
                </div>
            </div>

        </div>
    </div>
</template>
<script>
import DataService from "../DataService";

export default {
    name: 'registration',
    data() {
        return {
            email: '',
            password: '',
            password2: '',
            alerts: []
        };
    },
    methods: {
        registration() {
            if (this.password !== this.password2) {
                this.alerts.push('A két jelszó nem egyezik, kérlek javítsd!')
            }

            DataService.SignUp({
                email: this.email,
                password: this.password
            }).then(
                r => {
                    this.$root.setUserMutation(r);
                    this.$router.push({name:"profile"});
                }
            ).catch(err => {
                this.alerts.push(err);
            });
        }
    }
}
</script>